  private void updateButtons() {
    final boolean copyFiles = myCopyLibraryFilesRadioButton.isSelected();
    myCopyToDir.setEnabled(copyFiles);
    setOKActionEnabled(!copyFiles || !myCopyToDir.getText().isEmpty());
  }

